     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $accounts = Account::where('email',$request->email)->first();
        if(!$accounts) {

           return response()->json(["result" => 'Email không tồn tại !!',"status"=>"200"],201);
        } 
        if($accounts){
            $Password = Account::where('password',$request->password)->first();
            if (!$Password) {
               return response()->json(["result" => 'Password is incorrect !!',"status"=>"200"],201);
            } else {
               return response()->json(["result" => 'OK!!',"status"=>"200"],201);
            }
        }
    }

